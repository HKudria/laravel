        $this->middleware('auth')->except('index','price','contact');
    }

    /**
     * Show the application dashboard.
     *
     * @return \Illuminate\Contracts\Support\Renderable
     */
    public function index()
    {
        return view('home');
    }

    public function price()
    {
        return view('price');
    }

    public function contact()
    {
        return view('contact');
    }
}
